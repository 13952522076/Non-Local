def constant_init(module, val, bias=0):
    nn.init.constant_(module.weight, val)
    if hasattr(module, 'bias') and module.bias is not None:
        nn.init.constant_(module.bias, bias)

def kaiming_init(module,
                 a=0,
                 mode='fan_out',
                 nonlinearity='relu',
                 bias=0,
                 distribution='normal'):
    assert distribution in ['uniform', 'normal']
    if distribution == 'uniform':
        nn.init.kaiming_uniform_(
            module.weight, a=a, mode=mode, nonlinearity=nonlinearity)
    else:
        nn.init.kaiming_normal_(
            module.weight, a=a, mode=mode, nonlinearity=nonlinearity)
    if hasattr(module, 'bias') and module.bias is not None:
        nn.init.constant_(module.bias, bias)

def last_zero_init(m):
    if isinstance(m, nn.Sequential):
        constant_init(m[-1], val=0)
        m[-1].inited = True
    else:
        constant_init(m, val=0)
        m.inited = True


class ContextMULBlock2d(nn.Module):

    def __init__(self, channel, reduction = 16):
        super(ContextMULBlock2d, self).__init__()
        self.conv_mask = nn.Conv2d(channel, 1, kernel_size=1)
        self.softmax = nn.Softmax(dim=2)
        self.channel_mul_conv = nn.Sequential(
            nn.Conv2d(channel, channel//reduction, kernel_size=1),
            nn.LayerNorm([channel//reduction, 1, 1]),
            nn.ReLU(inplace=True),
            nn.Conv2d(channel//reduction, channel, kernel_size=1)
        )

        # init
        kaiming_init(self.conv_mask, mode='fan_in')
        self.conv_mask.inited = True
        last_zero_init(self.channel_mul_conv)

    def context_modeling(self,x):
        N, C, H, W = x.size()
        g = x
        g = g.view(N, C, H * W)  # [N,C,H*W]
        phi = self.conv_mask(x)  # [N,1,H,W]
        phi = phi.view(N, 1, H * W)  # [N,1,H*W]
        phi = self.softmax(phi)  # [N,1,H*W]
        phi = phi.permute(0, 2, 1)  # [N,H*W,1]
        context = torch.matmul(g, phi)  # [N,C,1]
        context = context.unsqueeze(-1)  # [N,C,1,1]
        return context

    def forward(self, x):
        # [N, C, 1, 1]
        context = self.context_modeling(x)
        out = x
        # [N, C, 1, 1]
        channel_mul_term = self.channel_mul_conv(context)
        channel_mul_term = torch.sigmoid(channel_mul_term)
        out = out * channel_mul_term
        return out
