"""
Non_Local ResNet (embedded Gaussian)
For CIFAR-100 dataset (32pixel X 32 pixel), we add non-local block for each residual block.
We set the inter_channel to C//2, sub_sample = Ture (W/2,H/2), BN for W.

Author:     Xu Ma.
Date:       Jul/23/2019
Email:      xuma@my.unt.edu
"""
